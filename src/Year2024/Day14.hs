part1 :: String -> Bounds -> Int
part1 s b =
    safetyFactor
        . map (advanceRobot 100)
        . mapMaybe (`parseRobot` b)
        . lines
        $ s

safetyFactor :: [Robot] -> Int
safetyFactor rs = rs1 * rs2 * rs3 * rs4
  where
    (q1, q2, q3, q4) = toQuadrants . getBounds . head $ rs
    rs1 = length $ filter (`isIn` q1) rs
    rs2 = length $ filter (`isIn` q2) rs
    rs3 = length $ filter (`isIn` q3) rs
    rs4 = length $ filter (`isIn` q4) rs

data Robot = Robot Position Velocity Bounds deriving (Show, Eq)

getBounds :: Robot -> Bounds
getBounds (Robot _ _ b) = b

data Bounds = Bounds Int Int deriving (Show, Eq)

data Quadrant = Quadrant (Int, Int) (Int, Int) deriving (Show, Eq)

data Velocity = Velocity Int Int deriving (Show, Eq)

toQuadrants :: Bounds -> (Quadrant, Quadrant, Quadrant, Quadrant)
toQuadrants (Bounds x y) =
    ( Quadrant (0, hx) (0, hy)
    , Quadrant (x - hx, x) (0, hy)
    , Quadrant (0, hx) (y - hy, y)
    , Quadrant (x - hx, x) (y - hy, y)
    )
  where
    hx = x `div` 2
    hy = y `div` 2

isIn :: Robot -> Quadrant -> Bool
isIn (Robot (Position x y) _ _) (Quadrant (x1, x2) (y1, y2)) =
    (x1 <= x && x < x2) && (y1 <= y && y < y2)

-- >>> advanceRobot 5 (Robot (Position 2 4) (Velocity 2 (-3)) (Bounds 11 7))
-- Robot (Position 1 3) (Velocity 2 (-3)) (Bounds 11 7)
advanceRobot :: Int -> Robot -> Robot
advanceRobot n r = foldl' (\r' _ -> robotStep r') r [0 .. (n - 1)]

-- >>> robotStep (Robot (Position 2 4) (Velocity 2 (-3)) (Bounds 11 7))
-- Robot (Position 4 1) (Velocity 2 (-3)) (Bounds 11 7)
robotStep :: Robot -> Robot
robotStep (Robot (Position x y) v@(Velocity vx vy) b@(Bounds bx by)) =
    Robot (Position ((x + vx) `mod` bx) ((y + vy) `mod` by)) v b

-- >>> parseRobot "p=0,4 v=3,-3" (Bounds 11 7)
-- Just (Robot (Position 0 4) (Velocity 3 (-3)))
parseRobot :: String -> Bounds -> Maybe Robot
parseRobot s b = do
    [pos, vel] <- Just $ words s
    [px, py] <- Just . map read . splitOn "," . drop 2 $ pos
    [vx, vy] <- Just . map read . splitOn "," . drop 2 $ vel
    return $ Robot (Position px py) (Velocity vx vy) b

