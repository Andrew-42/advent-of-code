part2 :: String -> Bounds -> IO ()
part2 s b =
    mapM_
        (\i -> printMap (getR i) . map (advanceRobot (getR i)) $ robots)
        [0 .. 100]
  where
    robots = mapMaybe (`parseRobot` b) . lines $ s
    getR i = i * 100 + i + 2

printMap :: Int -> [Robot] -> IO ()
printMap i rs = case pGrid of
    Just (Grid2D ls) -> putStrLn $ unlines (heading : ls)
    Nothing -> putStrLn "Nothing"
  where
    heading = "Second: " ++ show i
    grid = Grid2D $ replicate 103 (replicate 101 '.')
    pGrid = setAllGridAt 'X' (map getPosition rs) grid

getPosition :: Robot -> Position
getPosition (Robot p _ _) = p

solve :: IO ()
solve = do
    content <- readFile "./src/Year2024/data/day14.txt"
    print $ "Part1 solution: " ++ show (part1 content (Bounds 101 103))
    print "Part2 solution:"
    part2 content (Bounds 101 103)
