part1 :: T.Text -> Int
part1 text = go (S.singleton $ start grid) 0
  where
    go :: S.Set Position -> Int -> Int
    go ps collisions
        | row (S.elemAt 0 ps) == maxRow = collisions
        | otherwise = go passedPs (collisions + nSplits)
      where
        passedPs = S.fromList . concatMap (pass grid) . down . S.toList $ ps
        nSplits = length . filter (isSplitter grid) . down . S.toList $ ps

    maxRow = nRows (shape grid)
    grid = pInput text

start :: Grid2D Char -> Position
start g = fromJust $ positionOf 'S' g

down :: [Position] -> [Position]
down = map (`next` North)

pass :: Grid2D Char -> Position -> [Position]
pass g p = if isSplitter g p then split g p else [p]

isSplitter :: Grid2D Char -> Position -> Bool
isSplitter g p = case gridAt p g of
    Just '^' -> True
    _ -> False

split :: Grid2D Char -> Position -> [Position]
split g p = filter (isEmpty g) [next p West, next p East]

isEmpty :: Grid2D Char -> Position -> Bool
isEmpty g p = case gridAt p g of
    Just '.' -> True
    _ -> False

