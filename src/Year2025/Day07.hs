part2 :: T.Text -> Int
part2 text = go [(start grid, 1)]
  where
    go :: [(Position, Int)] -> Int
    go ps
        | row (fst $ head ps) == maxRow = sum . map snd $ ps
        | otherwise = go passedPs
      where
        passedPs =
            map (\ps' -> (fst . head $ ps', sum . map snd $ ps'))
                . groupBy id
                . concatMap (pass' grid)
                . down'
                $ ps

    maxRow = nRows (shape grid)
    grid = pInput text

down' :: [(Position, Int)] -> [(Position, Int)]
down' = map (\(p, n) -> (p `next` North, n))

pass' :: Grid2D Char -> (Position, Int) -> [(Position, Int)]
pass' g (p, n) =
    if isSplitter g p
        then map (\p' -> (p', n)) $ split g p
        else [(p, n)]

pInput :: T.Text -> Grid2D Char
pInput = Grid2D . lines . T.unpack

